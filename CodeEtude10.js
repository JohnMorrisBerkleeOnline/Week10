let number1= '';
let number2= '';
let operator = '';
let total= '';

$(document) .ready(function() {
  $('button') .on('click',function(e) {
    let btn = e.target.innerHTML;
    if (btn >= '0' && btn <= '9') {
      handleNumber(btn);
     } else {
       handleOperator(btn);
     }
   });
});

function handleNumber(num) {
    if (number1 === '') {
        number1 = number;
    } else {
        number2 = number;
    }
    displayButton(number);
}

function handleOperator(operator) {
    if (operator === '') {
        operator = operator;
    } else {
        handleTotal();
        operator = operator;
    }
}

function handleTotal() {
    switch (operator) {
        case '+':
            total = +number1 + +number2;
            displayButton(total);
            break;
        case '-':
            total = +number1 - +number2;
            displayButton(total);
            break;
        case '/':
            total = +number1 / +number2;
            displayButton(total);
            break;
        case 'X':
            total = +number1 * +number2;
            displayButton(total);
            break;
    }
    updateVariables();
}

function displayButton(btn) {
    $('.calc-result-input').text(btn);
}
function updateVariables() {
    number1 = total;
    number2 = '';
}
